package Hafta1;

public class DegiskeneDegerAtama {
public static void main(String[] args) {
	
	int x = 78;
	System.out.println(x + 1);
	System.out.println(x);
	
	x = x + 1; // esitlik varsa deger ataması olur
	System.out.println(x);
	
	int y = 2;
	System.out.println(y++); // islemi yap sonra degeri arttir
	System.out.println(y);
	
	int z = 5;
	System.out.println(++z); // degeri arttır islemi yap
	
}
}
